namespace LSDW.Enumerators;

/// <summary>
/// The mission status type enumerator.
/// </summary>
public enum MissionStatusType
{
	/// <summary>
	/// The mission status is unknown.
	/// </summary>
	Unknown = 0,
	/// <summary>
	/// The mission status is runable.
	/// </summary>
	Runable = 1,
	/// <summary>
	/// The mission status is started.
	/// </summary>
	Started = 2,
	/// <summary>
	/// The mission status is stopped.
	/// </summary>
	Stopped = 3,
	/// <summary>
	/// The mission status is aborted.
	/// </summary>
	Aborted = 4
}
